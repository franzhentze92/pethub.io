/** Combina ficha base con enriquecimiento sin sobrescribir valores ya definidos. */
function applyMicroPatch(
  food: GuatemalaReferenceFood,
  patch: Partial<GuatemalaReferenceFood> | undefined,
): GuatemalaReferenceFood {
  if (!patch) return food;

  const merged: GuatemalaReferenceFood = { ...food };

  for (const key of NUMERIC_MICRO_KEYS) {
    const patchVal = patch[key];
    const baseVal = merged[key];
    if (patchVal != null && (baseVal == null || baseVal === undefined || Number(baseVal) <= 0)) {
      (merged as Record<string, unknown>)[key] = patchVal;
    }
  }

  if (patch.data_source) {
    if (!food.data_source || food.data_source.length < 40) {
      merged.data_source = patch.data_source;
    } else if (!food.data_source.includes('aditivos') && !food.data_source.includes('gap-fill')) {
      merged.data_source = `${food.data_source} · ${patch.data_source.split('·').pop()?.trim() ?? patch.data_source}`;
    }
  }

  return merged;
}

function fillMicroGapsFromDefaults(food: GuatemalaReferenceFood): GuatemalaReferenceFood {
  const brandDefaults = resolveBrandMicroDefaults(food);
  const categoryDefaults = resolveCategoryMicroDefaults(food);
  let merged = food;
  let filled = false;

  const before = JSON.stringify(
    NUMERIC_MICRO_KEYS.map((k) => merged[k]),
  );

  merged = applyMicroPatch(merged, brandDefaults);
  merged = applyMicroPatch(merged, categoryDefaults);

  const after = JSON.stringify(
    NUMERIC_MICRO_KEYS.map((k) => merged[k]),
  );

  if (before !== after) {
    filled = true;
    if (!merged.data_source.includes('gap-fill')) {
      merged.data_source = `${merged.data_source} · PetHub gap-fill marca/categoría`;
    }
  }

  return filled ? merged : food;
}

export function enrichReferenceFood(food: GuatemalaReferenceFood): GuatemalaReferenceFood {
  const patch = REFERENCE_FOOD_MICRO_ENRICHMENT[food.id];
  const withIdPatch = applyMicroPatch(food, patch);
  return fillMicroGapsFromDefaults(withIdPatch);
}

export function enrichReferenceFoods(foods: GuatemalaReferenceFood[]): GuatemalaReferenceFood[] {
  return foods.map(enrichReferenceFood);
}
